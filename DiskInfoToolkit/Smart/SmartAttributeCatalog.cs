        #region Public

        public static IReadOnlyDictionary<byte, SmartAttributeDefinition> GetDefinitions(SmartAttributeProfile profile)
        {
            if (Mapping.TryGetValue(profile, out IReadOnlyDictionary<byte, SmartAttributeDefinition> definitions))
            {
                return definitions;
            }

            if (Mapping.TryGetValue(SmartAttributeProfile.Smart, out IReadOnlyDictionary<byte, SmartAttributeDefinition> defaultDefinitions))
            {
                return defaultDefinitions;
            }

            return new Dictionary<byte, SmartAttributeDefinition>();
        }

        public static SmartAttributeDefinition GetDefinition(SmartAttributeProfile profile, byte attributeId)
        {
            if (profile != SmartAttributeProfile.Unknown
                && Mapping.TryGetValue(profile, out IReadOnlyDictionary<byte, SmartAttributeDefinition> profileDefinitions)
                && profileDefinitions.TryGetValue(attributeId, out SmartAttributeDefinition profileDefinition))
            {
                return profileDefinition;
            }

            if ((profile == SmartAttributeProfile.Unknown || profile == SmartAttributeProfile.SSD)
                && Mapping.TryGetValue(SmartAttributeProfile.SSD, out IReadOnlyDictionary<byte, SmartAttributeDefinition> ssdDefinitions)
                && ssdDefinitions.TryGetValue(attributeId, out SmartAttributeDefinition ssdDefinition))
            {
                return ssdDefinition;
            }

            if (Mapping.TryGetValue(SmartAttributeProfile.Smart, out IReadOnlyDictionary<byte, SmartAttributeDefinition> genericDefinitions)
                && genericDefinitions.TryGetValue(attributeId, out SmartAttributeDefinition genericDefinition))
            {
                return genericDefinition;
            }

            return null;
        }

        public static string GetDisplayName(SmartAttributeProfile profile, byte attributeId, ILocalizedTextProvider textProvider = null)
        {
            var definition = GetDefinition(profile, attributeId);
            if (definition == null)
            {
                return BuildUnknownAttributeName(attributeId);
            }

            var effectiveTextProvider = textProvider ?? Storage.GetTextProvider();

            var localized = effectiveTextProvider != null ? StringUtil.TrimStorageString(effectiveTextProvider.GetText(definition.TextKey)) : string.Empty;
            if (!string.IsNullOrWhiteSpace(localized))
            {
                return localized;
            }

            return BuildUnknownAttributeName(attributeId);
        }

        public static string BuildUnknownAttributeName(byte attributeId)
        {
            return $"Unknown SMART Attribute 0x{attributeId:X2}";
        }

